# Dizionario delle lingue disponibili
# Mappa i codici lingua (es. 'en', 'it') ai rispettivi dizionari di traduzione
LANGUAGES = {
    "en": en,  # Inglese
    "it": it   # Italiano
}

# Variabile globale per la lingua corrente
_current_language = "it"

def set_language(language_code):
    """
    Imposta la lingua corrente dell'applicazione.
    
    Args:
        language_code (str): Codice della lingua da impostare (es. 'en', 'it')
        
    Returns:
        bool: True se la lingua Ã¨ stata impostata con successo, False altrimenti
    """
    global _current_language
    if language_code in LANGUAGES:
        _current_language = language_code
        return True
    return False

def get_string(key):
    """
    Restituisce la stringa tradotta nella lingua corrente.
    
    Args:
        key (str): Chiave della stringa da tradurre
        
    Returns:
        str: La stringa tradotta o la chiave stessa se non trovata
    """
    try:
        return LANGUAGES[_current_language].get(key, key)
    except KeyError:
        return key
